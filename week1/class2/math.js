
let rand16 = Math.floor((Math.random()*6)+1); // print random number between 1 to 6
console.log("Random between 1 to 6 :",rand16)

// Math.random    0-1
let random = Math.random()
console.log("random : ",random) 

// Math.floor    
let floor = Math.floor(4.59)
console.log("floor : ",floor) 

// Math.ceil    
let ceil = Math.ceil(4.59)
console.log("Ceiling : ",ceil) 

// Math.round    
let round = Math.round(4.49)
console.log("round off :",round)

// Math.min    
let min = Math.min(12,5,9,4,6,2)
console.log("Minimun :",min)

// Math.max    
let max = Math.max(12,5,9,4,6,2)
console.log("Maximum :",max)

// Math.pow    
let power = Math.pow(3,3)
console.log("power :",power)

// Math.sqrt
let sqrt = Math.sqrt(21)
console.log("square root :",sqrt)